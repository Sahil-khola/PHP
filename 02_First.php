What is echo in php?
Ans : The echo statement is used to output a string to the screen.

What is Variable in php?
Ans : A variable is a container for storing data values.

What is Comment in php?
Ans : A comment is a piece of text that the interpreter ignores.

What are Data Type and types?
Ans : Data types are the categories of values that a variable can hold.
types are int , string , float , boolean , array , object

What is Constant in Php?
Ans : Constant is a value that cannot be changed during the execution of a program.

What is Function in php?
Ans : A function is a block of code that performs a specific task and can be reused multiple times in a program.

What is Operator in php?
Ans : An operator is a symbol that performs a specific operation on one or more operands.

What is Array in php?
Ans : An array is a collection of elements of the same type stored at contiguous memory locations.

What is Loop in php?
Ans : A loop is a control structure that allows you to execute a block of code multiple times.

What is If-Else in php?
Ans : An if-else statement is a conditional statement that executes one block of code if a condition is true, and another block of code if the condition is false.

What is Switch in php?
Ans : A switch statement is a control structure that allows you to execute different blocks of code based on different conditions.

What is Null in php?
Ans : Null is a special value in PHP that represents the absence of a value.



